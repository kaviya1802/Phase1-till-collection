package com.simplilearn.loops;

public class WhileLoop {

	public static void main(String[] args) {
		// While Loop : Simple counter example
//		While with true is always running loop - Hence we need to terminate the condition
		int count = 0;
		while(count<10) {
			System.out.println("The count value is :: "+count);
			//Terminator
			count++;
		}

	}

}
