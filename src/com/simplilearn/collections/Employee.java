package com.simplilearn.collections;

public class Employee {
	public int empId;
	public String empName;
	public double salary;
	public String dept;
	public Employee(int empId, String empName, double salary, String dept) {
		super();
		this.empId = empId;
		this.empName = empName;
		this.salary = salary;
		this.dept = dept;
	}
	

}
